package topics

fun main() { // fun - функция

    // Котлин, как и Java, строго типизированный язык
    // Константа - неизменяемая переменная
    // Переменная - изменяемая переменная

    val test1: String = "Hello" // val - константа
    val test3 = "Hello" // тип можно не указывать

    var test2: String = "World" // var - переменная
    var test4 = "World" // тип можно не указывать

    println("Velue of test1: $test1") //$ - конкатенирует строки
    println("Sum of 1 and 2: ${1 + 2}") //$ - конкатенирует Котлин код
}